/// App spacing constants
/// 
/// This class defines all spacing values used throughout the application.
/// Using a centralized spacing system ensures consistency across the app.
class AppSpacing {
  // Spacing values
  static const double xs = 4.0;
  static const double s = 8.0;
  static const double m = 16.0;
  static const double l = 24.0;
  static const double xl = 32.0;
  static const double xxl = 48.0;
  static const double xxxl = 64.0;
  
  // Common padding values
  static const double screenPadding = m;
  static const double cardPadding = m;
  static const double buttonPadding = m;
  
  // Common margin values
  static const double itemSpacing = m;
  static const double sectionSpacing = xl;
  
  // Border radius values
  static const double borderRadiusSmall = 4.0;
  static const double borderRadiusMedium = 8.0;
  static const double borderRadiusLarge = 16.0;
}